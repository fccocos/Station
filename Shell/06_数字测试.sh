#!/bin/bash

echo "请输入一个数字num1>>>"
read num1
echo "请输入一个数字num2>>>"
read num2

[ $num2 -eq $num1 ]
echo " num1==num2? $?"

[ $num1 -ne $num2 ]
echo " num1!=num2? $?"

[ $num1 -ge $num2 ]
echo " num1>=num2? $?"

[ $num1 -le $num2 ]
echo "num1<=num2 $?"

test $num1 -gt $num2
echo " num1>num2? $?"

test $num1 -lt $num2
echo " num1<num2? $?"


